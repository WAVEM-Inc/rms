
class EventInfo():
    def __init__(self, eventId=None, eventCd=None, eventSubCd=None, areaClsf=None, floor=None, batteryLevel=None) -> None:
        self.eventId = eventId
        self.eventCd = eventCd
        self.eventSubCd = eventSubCd
        self.areaClsf = areaClsf
        self.floor = floor
        self.batteryLevel = batteryLevel